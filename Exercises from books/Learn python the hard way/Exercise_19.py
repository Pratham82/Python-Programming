def cheese_crackers(cheese_count, crackers_count):
    print(f"You have {cheese_count} chesses. ")
    print(f"You have {crackers_count} chesses. ")
    print("It's enough for party!!")
    print("Get a blanket\n")

print("We can just give the functions numbers directly")
cheese_crackers(23,353)

print("Or we can use varibales from our script")
cheese_n = 232
crackers_n = 23
cheese_crackers(cheese_n,crackers_n)

print("We can do math inside the fucntions too")
cheese_crackers(100+43,34+23)

print("We can combine all these methods")
cheese_crackers(cheese_n+100+10,crackers_n+10+30)

